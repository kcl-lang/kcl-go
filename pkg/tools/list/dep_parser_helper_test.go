func TestDepParser_parseImport(t *testing.T) {
	for i, tt := range t_parser_import_list {
		imports := parseImport(tt.code)
		if len(imports) != tt.imports {
			t.Fatalf("%d: parseImport failed, imports = %v, code = %s", i, imports, tt.code)
		}
	}
}

